    /**
     * 점수 범위에 따라 적절한 텍스트 배열 반환
     */
    static String[] getTextsByScore(String category, int score) {
        String[][] tiers = switch (category) {
            case "overall" -> new String[][]{
                OVERALL_TERRIBLE, OVERALL_BAD, OVERALL_NORMAL, OVERALL_GOOD, OVERALL_EXCELLENT
            };
            case "wealth" -> new String[][]{
                WEALTH_TERRIBLE, WEALTH_BAD, WEALTH_NORMAL, WEALTH_GOOD, WEALTH_EXCELLENT
            };
            case "love" -> new String[][]{
                LOVE_TERRIBLE, LOVE_BAD, LOVE_NORMAL, LOVE_GOOD, LOVE_EXCELLENT
            };
            case "health" -> new String[][]{
                HEALTH_TERRIBLE, HEALTH_BAD, HEALTH_NORMAL, HEALTH_GOOD, HEALTH_EXCELLENT
            };
            default -> throw new IllegalArgumentException("Unknown category: " + category);
        };

        int tier;
        if (score <= 20) tier = 0;
        else if (score <= 40) tier = 1;
        else if (score <= 60) tier = 2;
        else if (score <= 80) tier = 3;
        else tier = 4;

        return tiers[tier];
    }
